package net.taken.project3960;

public class Player {

    private float x = 0f;
    private float y = 0f;
    private float z = 1f;
    private float theta = 0f; // Plane (x, y)
    private float rho = 0f; // Plane (x, z)

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public float getZ() {
        return z;
    }

    public float getTheta() {
        return theta;
    }

    public float getRho() {
        return rho;
    }
}
